#include <unistd.h>
#include <string.h>
#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] || s2[i])
	{
		if ((unsigned char)s1[i] != (unsigned char)s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return (0);
}

int	main(void)
{
	char	*s1= "Davut";
	char	*s2= "Davut Uzun";

	printf("org :%d\n", strcmp(s1, s2));
	printf("emu :%d\n", ft_strcmp(s1, s2));
	return (0);
}
