#include <unistd.h>

int	is_power_of_2(unsigned int n)
{
	if ((int)n <= 0)
		return (0);
	if (n == 1)
		return (1);
	if (n % 2 != 0)
		return (0);
	while (n >= 2)
	{
		if (n % 2 != 0)
			return (0);
		n /= 2;
	}
	return (1);
}

