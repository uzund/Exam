#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	*s;
	char	c;

	if (ac == 2)
	{
		i = 0;
		s = av[1];
		while (s[i] != '\0')
		{
			if (s[i] >= 'A' && s[i] <= 'Z')
			{
				c = (90 - s[i]) + 65;
				write(1, &c, 1);
			}
			else if (s[i] >= 'a' && s[i] <= 'z')
			{
				c = (122 - s[i]) + 97;
				write(1, &c, 1);
			}
			else
				write(1, &s[i], 1);
			i++;
		}
		write(1, "\n", 1);
		return (0);
	}
	else
	{
		write(1, "\n", 1);
		return (0);
	}
}
