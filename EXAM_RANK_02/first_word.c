#include <unistd.h>

int	main(int ac, char **av)
{
	int		i;
	char	*s;

	if (ac == 2)
	{
		i = 0;
		s = av[1];
		while ((s[i] >= 9 && s[i] <= 13) || s[i] == ' ')
			i++;
		while (s[i] != '\0' && s[i] != ' ')
		{
			write(1, &s[i], 1);
			i++;
		}
		write(1, "\n", 1);
		return (0);
	}
	else
	{
		write(1, "\n", 1);
		return (0);
	}
}
